"""Pull-knowledge primitives: what KonfAI can do, and the schema of a workflow config.

These are deliberately *thin* and *generated*, not prose duplicated from the docs:

* ``describe_konfai_capabilities`` is a hub that points the agent at the right tools/resources
  (and at AGENTS.md as the single source of truth) instead of re-encoding KonfAI's rules.
* ``describe_config_schema`` is generated by introspecting the Trainer/Predictor/Evaluator
  constructors via the reflection engine, so it never drifts from the actual config contract.

The intent is the right abstraction level: give the agent capabilities + a schema to reason
about, not a step-by-step workflow to execute.
"""

from __future__ import annotations

import importlib
import inspect
import types
from typing import Any, Union, get_args, get_origin

from konfai_mcp.workflows import WORKFLOW_SPECS

